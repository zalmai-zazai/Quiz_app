function deselectAnswers() {
  qs.forEach(answerEl => answerEl.checked = false)
}
function getSelected() {
  let answer
  qs.forEach(answerEl => {
      if(answerEl.checked) {
          answer = answerEl.id
      }
  })
  return answer
}


next.addEventListener('click',(evt)=>{
  
  const answer = getSelected()
  if(answer) {
     if(answer === javaScriptQuizData[currentQuiz].correct ||answer === pHpQuizData[currentQuiz].correct ||answer === cpQuizData[currentQuiz].correct ) {
         score++
         deselectAnswers()
     }
     currentQuiz++
     if(currentQuiz < javaScriptQuizData.length ||currentQuiz < pHpQuizData.length ||currentQuiz < cpQuizData.length) {
         loadQuiz(javaScriptQuizData)
         loadQuiz(pHpQuizData)
         loadQuiz(cpQuizData)
         deselectAnswers()
     } else {
         quiz.innerHTML = `
         <h2>You answered ${score}/${javaScriptQuizData.length} questions correctly</h2>
         <button onclick="location.reload()">Reload</button>
         `
     }
  }

 
})

